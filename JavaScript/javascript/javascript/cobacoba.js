function kroketAyam (jumlahPack){
    const pcsPerPack = 3;
    return pcsPerPack * jumlahPack;
}
console.log(`jumlah pack kroketayam ada ${kroketAyam(13)} pcs`)

function kroketTelur(jumlahBanyak) {
    const pcsPack = 3
    return pcsPack * jumlahBanyak;  
}
 console.log(`jumlah pack krokettelur ada ${kroketTelur(13)} pcs`)

